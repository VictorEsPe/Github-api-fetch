const screen = {
  userProfile: document.querySelector('.profile-data'),
  renderUser(user) {
    this.userProfile.innerHTML = `<div class="info"> 
                                    <img src="${
                                      user.avatarUrl
                                    }" alt="Foto de perfil do usu치rio"/>
                                    <div class="data">
                                      <h1>${
                                        user.name ??
                                        'N칚o possui nome cadastrado 游땬'
                                      }</h1>
                                      <p>${
                                        user.bio ??
                                        'N칚o possui bio cadastrada 游땬'
                                      }</p>
                                      <div>
                                        <p>${user.followers} Seguidores</p>
                                        <p>${user.following} Seguindo</p>
                                      </div>
                                    </div>
                                  </div>`;

    let repositoriesItens = '';
    user.repositories.forEach(
      repo =>
        (repositoriesItens += `<li><a href="${repo.html_url}" target="_blank">${repo.name}</a></li>`)
    );

    if (user.repositories.length > 0) {
      this.userProfile.innerHTML += `<div class="repositories section">
                                        <h2>Reposit칩rios</h2>
                                        <ul>${repositoriesItens}</ul>
                                     </div>`;
    }

    this.renderEvents(user.events);
  },

  renderEvents(events) {
    if (events.length === 0) {
      return (this.userProfile.innerHTML +=
        '<p>Usu치rio ainda sem eventos 游땬</p>');
    }

    let eventsItens = '';
    events.forEach(event => {
      const isPushOrCreateEvent =
        event.type === 'PushEvent' || event.type === 'CreateEvent';

      if (isPushOrCreateEvent) {
        if (event.type === 'PushEvent') {
          eventsItens += `<li><h3>${event.repo.name}</h3><p>- ${event.payload.commits[0].message}</p></li>`;
        }
        if (event.type === 'CreateEvent') {
          eventsItens += `<li><h3>${event.repo.name}</h3><p>- Sem mensagem de commit</p></li>`;
        }
      } else return;
    });

    this.userProfile.innerHTML += `<div class="events section">
                                    <h2>Eventos</h2>
                                    <ul>${eventsItens}</ul>
                                  </div>`;
  },

  renderNotFound() {
    this.userProfile.innerHTML = '<p>Usu치rio n칚o encontrado 游땬</p>';
  },
};

export { screen };
